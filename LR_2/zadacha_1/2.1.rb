class Student
  def initialize(name,midleName,lastName)
    @id
    @lastName = lastName
    @name = name
    @midleName = midleName
    @telegram
    @phone
    @email
    @git

  end
  def id
    @id
  end
  def id=(id)
    @id=id
  end
  def name
    @name
  end
  def name=(name)
    @name=name
  end
  def midlename
    @midleName
  end
  def midlename=(midleName)
    @midleName = midleName
  end
  def lastname
    @lastName
  end
  def lastname=(lastName)
    @lastName=lastName
  end
  def phone
    @phone
  end
  def phone=(phone)
    @phone=phone
  end
  def telegram
    @telegram
  end
  def telegram=(telegram)
    @telegram=telegram
  end
  def email
    @email
  end
  def email=(email)
    @email=email
  end
  def git
    @git
  end
  def git=(git)
    @git=git
  end
end
